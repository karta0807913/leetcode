func longestSubarray(nums []int) int {
	maxVal := 0
	for _, num := range nums {
		if num > maxVal {
			maxVal = num
		}
	}
	found := false
	length := 0
	maxLength := 1
	for _, num := range nums {
		if num == maxVal {
			found = true
			length += 1
		} else {
			if length > maxLength {
				maxLength = length
			}
			if found {
				length = 0
			}
		}
	}
	if length > maxLength {
		maxLength = length
	}
	return maxLength
}

